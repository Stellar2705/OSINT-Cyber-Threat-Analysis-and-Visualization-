def _kw_weight(sector: str, kw: str) -> float:
    """
    Heuristic weights:
      - Strong hints: 3.0
      - Multi-word operator/org names: 2.0
      - Generic tokens: 1.0
    """
    k = kw.lower()
    if k in _STRONG_HINTS.get(sector, set()):
        return 3.0
    if " " in k and len(k) >= 6:
        return 2.0
    return 1.0

def sector_raw_scores(text: str) -> dict:
    """Sum weighted keyword hits per sector (raw unnormalized score)."""
    t = text.lower()
    scores = {}
    for sector, kws in CII_SECTOR_KEYWORDS.items():
        s = 0.0
        for kw in kws:
            if kw in t:
                s += _kw_weight(sector, kw)
        scores[sector] = s
    return scores

def _normalize_score(raw: float, k: float = 6.0) -> float:
    """
    Soft saturation mapping raw→[0,1]:
        conf = raw / (raw + k)
    k controls how fast it saturates (6 works well for these dictionaries).
    """
    if raw <= 0:
        return 0.0
    return raw / (raw + k)

def sector_scores(text: str) -> dict:
    """Return sector→confidence (0..1) for all sectors."""
    raw = sector_raw_scores(text)
    return {sec: _normalize_score(v) for sec, v in raw.items()}

def tag_sector_with_confidence(text: str) -> tuple[str, float]:
    """Return (best_sector, best_confidence)."""
    confs = sector_scores(text)
    if not confs:
        return ("Other", 0.0)
    best_sector = max(confs, key=confs.get)
    best_conf = confs[best_sector]
    return (best_sector if best_conf > 0 else "Other", float(best_conf))



