def pop_frontier_batch(conn, n=100):
    cur = conn.cursor()
    rows = cur.execute("SELECT url, domain FROM frontier ORDER BY priority DESC LIMIT ?", (n,)).fetchall()
    for url, _ in rows:
        cur.execute("UPDATE frontier SET last_tried=? WHERE url=?", (datetime.utcnow().isoformat(), url))
    conn.commit()
