def poll_rss(feed_url: str):
    items = []
    if not allowed_by_robots(feed_url):
        return items
    fp = feedparser.parse(feed_url)
    for e in fp.entries[:50]:
        url = getattr(e, "link", None)
        title = getattr(e, "title", "") or ""
        body  = ""
        if hasattr(e, "summary"):
            body = BeautifulSoup(e.summary, "html.parser").get_text(" ", strip=True)
        elif hasattr(e, "content") and e.content:
            body = BeautifulSoup(e.content[0].value, "html.parser").get_text(" ", strip=True)
        published = getattr(e, "published", "") or ""
        items.append({
            "url": url, "title": title, "body": body, "published": published,
            "source": "rss", "seed": feed_url
        })
    return items

def fetch_article(url: str):
    r = http_get(url)
    if not r:
        return {"url": url, "title": "", "body": ""}
    soup = BeautifulSoup(r.text, "html.parser")
    title = soup.title.string.strip() if soup.title and soup.title.string else ""
    paragraphs = [p.get_text(" ", strip=True) for p in soup.find_all("p")]
    body = " ".join(paragraphs)
    time.sleep(SLEEP_BETWEEN_REQUESTS)
    return {"url": url, "title": title, "body": body}

def crawl_html_seed(seed_url: str, per_seed: int = 25):
    out = []
    r = http_get(seed_url)
    if not r: return out
    for link in extract_links(seed_url, r.text, max_links=per_seed):
        art = fetch_article(link)
        if len((art["title"] + " " + art["body"]).strip()) >= 120:
            art.update({"source":"html", "seed": seed_url, "published": ""})
            out.append(art)
    return out

