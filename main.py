IOC_PATTERNS = {
    "ipv4": re.compile(r"\b(?:\d{1,3}\.){3}\d{1,3}\b"),
    "domain": re.compile(r"\b(?:(?!-)[A-Za-z0-9-]{1,63}(?<!-)\.)+(?:[A-Za-z]{2,63})\b"),
    "url": re.compile(r"\bhttps?://[^\s<>'\"()]+", re.I),
    "sha256": re.compile(r"\b[a-fA-F0-9]{64}\b"),
    "md5": re.compile(r"\b[a-fA-F0-9]{32}\b"),
    "cve": re.compile(r"\bCVE-\d{4}-\d{4,7}\b", re.I),
}

def deobfuscate(text: str) -> str:
    t = re.sub(r"hxxp", "http", text, flags=re.I)
    t = t.replace("[.]", ".").replace("(.)", ".")
    t = re.sub(r"\s+\.\s+", ".", t)
    return t

def clean_text(title: str, body: str) -> str:
    t = f"{title or ''}. {body or ''}".strip()
    t = re.sub(r"\s+", " ", t)
    return deobfuscate(t)

def extract_iocs(text: str) -> dict:
    t = deobfuscate(text)
    out = {}
    for k, rx in IOC_PATTERNS.items():
        out[k] = list(set(rx.findall(t)))[:50]
    return out

